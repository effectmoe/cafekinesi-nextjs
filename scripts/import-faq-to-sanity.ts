async function importFAQs() {
  console.log('🚀 FAQ インポートを開始します...')
  console.log(`📊 インポート対象: ${faqs.length}件のFAQ`)

  let successCount = 0
  let errorCount = 0

  for (const faq of faqs) {
    try {
      // カテゴリーを英語キーに変換
      const categoryValue = categoryMap[faq.category]

      if (!categoryValue) {
        console.error(`❌ カテゴリーマッピングエラー: "${faq.category}" が見つかりません`)
        errorCount++
        continue
      }

      // Sanityにドキュメントを作成
      const doc = {
        _type: 'faq',
        _id: faq.id, // 既存のIDを使用
        question: faq.question,
        answer: faq.answer,
        category: categoryValue,
        order: parseInt(faq.id.split('-')[1]), // "kinesi-001" → 1
      }

      await publicClient.createOrReplace(doc)

      console.log(`✅ インポート成功: ${faq.id} - ${faq.question}`)
      successCount++
    } catch (error) {
      console.error(`❌ インポート失敗: ${faq.id}`, error)
      errorCount++
    }
  }

  console.log('\n📈 インポート結果:')
  console.log(`  ✅ 成功: ${successCount}件`)
  console.log(`  ❌ 失敗: ${errorCount}件`)
  console.log(`  📊 合計: ${faqs.length}件`)
}

// スクリプト実行
importFAQs()
  .then(() => {
    console.log('\n✅ FAQインポートが完了しました！')
    process.exit(0)
  })
  .catch((error) => {
    console.error('\n❌ FAQインポートでエラーが発生しました:', error)
    process.exit(1)
  })
