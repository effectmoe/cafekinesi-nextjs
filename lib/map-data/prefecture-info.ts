// Helper function to get prefecture info by name
export function getPrefectureByName(name: string) {
  return Object.values(prefectureInfo).find((pref) => pref.name === name)
}

// Helper function to get all prefectures in a region
export function getPrefecturesByRegion(region: string) {
  return Object.entries(prefectureInfo)
    .filter(([_, pref]) => pref.region === region)
    .map(([id, pref]) => ({ id, ...pref }))
}
